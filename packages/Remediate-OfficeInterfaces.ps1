<#
  .SYNOPSIS
  Copyright (c) Microsoft Corporation. All rights reserved.
  Detection and remediation for corrupt Office Interfaces values

  .DESCRIPTION
  This script scans the registry for Office 32-bit interface definitions
  that are corrupt (empty) and removes them.

  .PARAMETER ScanOnly
  Instructs the script to run in ScanOnly mode, disabling automatic remediation.
  This switch is set on by default and must be disabled to enable automatic 
  remediation.

  .INPUTS
  None. You cannot pipe objects to this script.

  .OUTPUTS
  Log output is written to: %windir%\temp\OfficeInterfaces.log

  .EXAMPLE
  PS> .\Remediate-OfficeInterfaces.ps1

  .EXAMPLE
  PS> .\Remediate-OfficeInterfaces.ps1 -ScanOnly:$false
#>

Param(
    [Parameter(Mandatory=$false)]

    [Switch]$ScanOnly = $true

)

# Log actions to %windir%\temp\OfficeInterfaces.log
function Log
{
    param(
        [Parameter(Mandatory=$true)][string]$logMessage
    )

    $LogFile = "$env:windir\Temp\OfficeInterfaces.log"
    $LogDate = get-date -format "MM/dd/yyyy HH:mm:ss"
    $LogLine = "$LogDate $logMessage"
    Add-Content -Path $LogFile -Value $LogLine -ErrorAction SilentlyContinue
}

# Cleans up Interface Hive
function ProcessInterfaceHive
{
    if ($ScanOnly)
    {
        Log("Running in ScanOnly mode")
    }
    
    $hive = "HKLM:\Software\WOW6432Node\Classes\Interface\"

    $interfacesKey = Get-Item -Path $hive

    ForEach ($interface in $arrInterfaces) 
    {
        $interfaceKey = $interfacesKey.OpenSubKey($interface)

        if ($interfaceKey -eq $null)
        {
            Log("No registration found for interface $interface")
        }
        else
        {
            Log("Found registration for interface $interface")
            
            $interfaceName = $interfaceKey.GetValue("")
            
            if ($interfaceName -eq "")
            {
                Log("Found interface $interface with corrupted (empty) name")

                $proxyStubKey = $interfaceKey.OpenSubKey("ProxyStubClsid32")

                if ($proxyStubKey -ne $null)
                {
                    $proxyStubValue = $proxyStubKey.GetValue("")
                    
                    if ($proxyStubValue -eq "")
                    {
                        Log("Found corrupted interface $interface")

                        if (!$ScanOnly)
                        {
                            Log("Removing $interface")
                            Remove-Item $interfaceKey -Force
                        }
                    }
                }
            }
        }
    }
}

